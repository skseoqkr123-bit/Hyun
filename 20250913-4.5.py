# 변수를 만드는 다양한 방법 
# 정수
var01=10; var02=20
# 만약 라인 하나에 명령어를 둘 이상 담고자 핢떄는 저렇게 콤마를 쓰면 된다. 

print(var01 + var02) ; print(var01 - var02)
print(var01 * var02) ; print(var01 / var02)

# 변수 설정을 추가로 활용할 수도 있음
#  Ex- var01=10+20, var02=20
# var03 = var01 + var02
# var04 = var01 
# '='는 인간에겐 이코르라는 의미이지만 컴퓨터에겐 대입연산자 라는 의미로 해석한다. 

var01=3.151592 # var01의 실수값 설정 
var02= True   #bool 
var03 = "good"
# 파이값이 가지는 형으로는 정수형, 실수형, 불(bool, boolean)형, 문자형이 있음

